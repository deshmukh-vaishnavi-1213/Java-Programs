
class CircleAP 
{
	public static void main(String[] args) 
	{
		
		float r = 5.5f;
		final float pie = 22/7;
		
		float area = pie*r*r;
		
		float perimeter = 2*pie*r;
		
		System.out.println("Area of circle with radius "+ r + "is :" + area );
		System.out.println("Perimeter of circle with radius "+ r + "is :" + perimeter );
	}
}

